    private final UUID CUSTOMER_ID = UUID.fromString("4d1bd47c-a534-4915-bf18-74fa3c963431");
    private final UUID RESTAURANT_ID = UUID.fromString("5e30b4e5-8d60-462c-b500-cbde0c0dbdd1");
    private final UUID PRODUCT_ID = UUID.fromString("bb2e1edc-a803-4dca-986c-26a9b64c54df");
    private final UUID ORDER_ID = UUID.fromString("e5398b50-8367-4c90-9f27-fe9dcd0c6123");
